# Create a For Loop with a Start and Stop value
print('1st Range with a Start and Stop arguments of 12 and 16:')
for i in range(12, 16):
    # Will print to screen 12 through 15
    print(i)

# Create a For Loop with a Start, Stop and Step value
print('2nd Range with a Start, Stop and Step argument of 0, 10, and 2:')
for i in range(0, 10, 2):
    # Will print to screen 0 through 8 every other #
    print(i)

# Create a For Loop with a Start, Stop and Step value
print('3rd Range with a Start, Stop and Step argument of 5, -1, and -1:')
for i in range(5, -1, -1):
    # Will print to screen 5 through -1 in descending order
    print(i)
