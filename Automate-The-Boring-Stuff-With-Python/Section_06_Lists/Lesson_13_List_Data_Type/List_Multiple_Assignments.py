# Define a List variable that contains Diablo 2 Class names
diablo2 = ['Shako', 'Anni Small Charm', 'Enigma Armor']

# Print the value to console
print(diablo2)

# Tuple Unpack the List value and assign the Index values to new Variables
druid, sorceress, paladin = diablo2

# Print the Variable values to console
print(diablo2)
print(druid)
print(sorceress)
print(paladin)
