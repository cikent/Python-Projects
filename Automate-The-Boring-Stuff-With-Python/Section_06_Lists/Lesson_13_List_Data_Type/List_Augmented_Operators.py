"""
Augument Operators for += && *= work with Strings and Lists to concatenate them
"""
# Declare a String variable
myD2Class = 'Druid'

# print the String to console
print(myD2Class)

# Use an Augment Operator to concatenate the String
myD2Class += ' is awesome!'

# print the String to console again
print(myD2Class)

# Declare a List variable
myD2Classes = ['Druid', 'Sorceress']

# print the List to console
print(myD2Classes)

# Use an Augment Operator to multiple the List
myD2Classes *= 2

# print the List to console
print(myD2Classes)
