# Define Variables
favoriteClass = 'Druid'
favoriteArmor = 'Enigma'

# Print the Variable values:
print('This is the initial value of the two variables: ' + '\n1st Variables = ' + favoriteClass + '\n2nd Variables = ' + favoriteArmor)

# Using Mutliple Assignment with Comma delimitation, you can swap variable values at the same time
favoriteClass, favoriteArmor = favoriteArmor, favoriteClass

# Print the Variable values:
print('This is the swapped values of the two variables: ' + '\n1st Variables = ' + favoriteClass + '\n2nd Variables = ' + favoriteArmor)